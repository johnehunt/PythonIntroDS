# Examples of simple functions in Python

# Illustrates some basic function definitions

def print_msg():
    """ A function that prints hello world """
    print('Hello World!')


print_msg()
print(type(print_msg))


def print_my_msg(msg):
    """ A simple function to print a message """
    print(msg)


print_my_msg('Hello World')
print_my_msg('Good day')
print_my_msg('Welcome')
print_my_msg('Ola')
